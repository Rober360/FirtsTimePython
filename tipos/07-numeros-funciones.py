import math

print(round(3.14159))  # Redondea al entero más cercano
print(round(3.84159))  # Redondea al entero más cercano
print(round(3.14159, 2))  # Redondea a dos decimales
print(abs(-10))  # Valor absoluto
print(abs(10))  # Valor absoluto

# Funciones de math
print(math.ceil(3.14159))  # Redondea al entero más grande
print(math.floor(3.14159))  # Redondea al entero más pequeño
print(math.isnan(16))  # ¿Es un número? R/ False
# print(math.isnan("23"))  # ¿Es un número? R/ True
print(math.pow(10, 3))  # Raíz cuadrada
print(math.sqrt(9))  # Raíz cuadrada
